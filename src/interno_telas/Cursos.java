     private void pesquisar_cursos() {
        String sql = "select * from cursos;";
        try {
            pst = conex.prepareStatement(sql);  
            rs = pst.executeQuery();
            //usando a biblio rs2xml.jar para preencher a tabela
            tblCursos.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
